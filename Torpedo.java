public class Torpedo{

    public int x;
    public int y;

    public Torpedo(int x, int y){
        this.x = x;
        this.y = y;
    }

    public void setX(int e){
        this.x = e;
    }
    
    public void setY(int e){
        this.y = e;
    }

    public int getX(){
        return this.x;
    }

    public int getY(){
        return this.y;
    }
    
}